    private void sendToDb(JLabel lb_name, String id) {
        Date da = new Date();

        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy");
        SimpleDateFormat sdf1 = new SimpleDateFormat("hh-mm-ss");
        String Fullname = "";
        String batch = "";
        String year = "";
        String cours = "";
        String firstTowName = "";
        String time = sdf1.format(da);
        String date = sdf.format(da);

        try {
            ResultSet name = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery(" SELECT * FROM `stu_info_name` where stu_user_info_id='" + id + "'");
            ResultSet batchAll = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery(" SELECT * FROM `stu_info_batch` where stu_user_info_id='" + id + "'");
            if (name.next()) {
                Fullname = name.getString("stu_info_name_first_name") + " " + name.getString("stu_info_name_last_name") + " " + name.getString("stu_info_name_sirName");
            }
            if (batchAll.next()) {
                batch = batchAll.getString("stu_info_batch");
                year = batchAll.getString("stu_info_year");
                cours = batchAll.getString("stu_info_course");
            }
            ResultSet rs = MC_JavaDataBaseConnection.myConnection().createStatement().executeQuery("select * from atendence where student_id='" + id + "' and date='" + date + "'");
            if (!(rs.next())) {
                
                MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate("INSERT INTO atendence (student_id,student_name,course,status,batch,year,date,time) VALUES ('" + id + "','" + Fullname + "','" + cours + "','1','" + batch + "','" + year + "','" + date + "','" + time + "')");
                firstTowName = name.getString("stu_info_name_first_name") + " " + name.getString("stu_info_name_last_name");
                lb_name.setText(firstTowName);
                lb_ok_id.setVisible(true);
                speekName(firstTowName);
                Thread.sleep(3000);
                lb_ok_id.setVisible(false);
                
            } else {
                if (!(rs.getString("out_state").equals("1"))) {
                    System.out.println("oooooo");
                    MC_JavaDataBaseConnection.myConnection().createStatement().executeUpdate("UPDATE `atendence` SET out_time='" + time + "',out_state='1' where student_id='"+id+"'");
                  
                    Voice voice;
                    VoiceManager vm = VoiceManager.getInstance();
                    voice = vm.getVoice(VOICENAME);

                    voice.allocate();

                    try {
                        firstTowName = name.getString("stu_info_name_first_name") + " " + name.getString("stu_info_name_last_name");
                         lb_name.setText(firstTowName);
                        voice.speak("Good Bye "+ firstTowName);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
                
                else{
                
                 Voice voice;
                    VoiceManager vm = VoiceManager.getInstance();
                    voice = vm.getVoice(VOICENAME);

                    voice.allocate();

                    try {
                        voice.speak("Sorry Invalide Entry");
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                
                }
                
                
                
                    
                
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

