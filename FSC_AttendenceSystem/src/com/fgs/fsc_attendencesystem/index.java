    private void jc_rfActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jc_rfActionPerformed

        if (flag) {
            System.out.println(flag);
            tf_rf.setEnabled(true);
            tf_rf.grabFocus();
            tf_qr.setEditable(false);
            tf_rf.setEditable(true);
            tf_manuwal.setEditable(false);
        }

        if (jc_rf.isSelected()) {
            jc_manual.setEnabled(false);
            jc_qr.setEnabled(false);
        }
        if (!(jc_rf.isSelected())) {
            jc_manual.setEnabled(true);
            jc_qr.setEnabled(true);
            tf_rf.setEnabled(false);
            tf_rf.setEditable(false);
        }

    }//GEN-LAST:event_jc_rfActionPerformed

